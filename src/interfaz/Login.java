    private void botonLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonLoginActionPerformed
        // TODO add your handling code here:
        String user = campoUsuario.getText();
        String clave = campoContra.getText();
        
        VerificarLogin resultados = new VerificarLogin();
        boolean acceso = resultados.VerificarDatos(user, clave);
        
        if(acceso){
            System.out.println("Acceso Exitoso");
            MenuEstudiantes lista = new MenuEstudiantes();
            //indicamos que la lista sea visible
            lista.setVisible(true);
            lista.setLocationRelativeTo(null);
            //cambiamos de pantalla
            dispose();
        }else{
            System.out.println("Acceso Denegado");
            resultadoError.setText("Usuario y/o Contraseña incorrecto(s).");
        }
        
    }//GEN-LAST:event_botonLoginActionPerformed

